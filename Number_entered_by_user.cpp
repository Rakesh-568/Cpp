/* Print the number entered by the user*/
#include <iostream>
using namespace std;
int main()
{
    int number;
    cout << "Enter a number:";
    cin >> number;                       // cin is used to scan the entered number
    cout << "Your number is:" << number; // cout is used to print the number
    return 0;
}

/* Output: 
Enter a number : 5
Your number is:5 */